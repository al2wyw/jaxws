package step1;

public class Book {
	private String name;
	private int price;
	private String id;
	public String getId(){
		return this.id;
	}
	public void setId(String id){
		this.id=id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}
